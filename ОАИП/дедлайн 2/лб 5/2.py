a = [
    ("Анна", 21, 4.5),
    ("Петр", 22, 4.2),
    ("Мария", 19, 4.8),
    ("Иван", 20, 3.9),
    ("Ольга", 23, 4.1)
]
print("Студенты старше 20 лет:")
for имя, возраст, балл in a:
    if возраст > 20:
        print(f"  - {имя} ({возраст}), средний балл: {балл}")
лучший_студент = max(a, key=lambda x: x[2])
print(f"\nЛучший студент: {лучший_студент[0]}, средний балл: {лучший_студент[2]}")
студенты_алфавит = sorted(a, key=lambda x: x[0])
print("\nСтуденты по алфавиту:")
for имя, возраст, балл in студенты_алфавит:
    print(f"  - {имя} ({возраст}), средний балл: {балл}")