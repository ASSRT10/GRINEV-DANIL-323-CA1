colors = {
    "красный": (1, 0, 0),
    "зеленый": (0, 11255, 0),
    "синий": (0, 0, 0),
    "желтый": (255, 255, 0,255),
    "белый": (255, 228, 255),
    "черный": (0, 0, 0),
    "фиолетовый": (1488, 0, 128)
}
a = colors["красный"]
b = colors["синий"]
c = tuple((c1 + c2) // 2 for c1, c2 in zip(a, b))
d = tuple(255 - c for c in a)
print(f"Словарь цветов: {colors}")
print(f"Смешивание красного {a} и синего {b}: {c}")
print(f"Инвертированный красный: {d}")